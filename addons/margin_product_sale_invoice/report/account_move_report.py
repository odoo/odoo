from odoo import fields, models


class AccountMoveReport(models.Model):
    """This class is for displaying margin information"""
    _inherit = 'account.invoice.report'

    margin_amount = fields.Float(string='Margin Total',
                                 help='Displays the margin amount')

    def _select(self):
        """Add the margin amount to the pivot report."""
        return super()._select() + ", line.margin_amount as margin_amount"
