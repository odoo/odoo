// @ts-check

/** @module @web/fields/specialized/user_groups/res_user_group_ids_privilege_field - Boolean/selection field for privilege toggles within the dynamically generated access rights form */

import { Component } from "@odoo/owl";
import { registry } from "@web/core/registry";
import { BooleanField } from "@web/fields/basic/boolean/boolean_field";
import { SelectionField } from "@web/fields/selection/selection/selection_field";
import { standardFieldProps } from "@web/fields/standard_field_props";
import { usePopover } from "@web/ui/popover/popover_hook";

import { ResUserGroupIdsPopover } from "./res_user_group_ids_popover";

/**
 * /!\ This widget is not meant to be used anywhere else than inside form view
 * dynamically generated by the res_user_group_ids widget.
 */

class ResUserGroupIdsPrivilegeField extends Component {
    static template = "web.ResUserGroupIdsPrivilegeField";
    static components = { BooleanField, SelectionField };
    static props = { ...standardFieldProps };

    setup() {
        /** @type {boolean} */
        this.isDebug = odoo.debug;
        this.popover = usePopover(ResUserGroupIdsPopover);
        this.groups = this.env.resUserGroupsInfo.groups;
    }

    /**
     * IDs of groups that conflict with the currently active group for this privilege.
     * @returns {number[]}
     */
    get disjointGroupIds() {
        const group = this.group || this.impliedGroup;
        return group ? group.disjointIds : [];
    }

    /**
     * The explicitly selected group for this privilege, or false if none.
     * @returns {Object | false}
     */
    get group() {
        const groups = this.groups;
        return groups[this.findGroupId((gid) => groups[gid].selected)] || false;
    }

    /**
     * The highest-level group implied (but not explicitly selected) for this privilege.
     * @returns {Object | false}
     */
    get impliedGroup() {
        const groups = this.groups;
        return (
            groups[this.findGroupId((gid) => groups[gid].impliedByIds.length > 0)] ||
            false
        );
    }

    /**
     * Display name of the implied group, or empty string if the field is explicitly set.
     * @returns {string}
     */
    get impliedGroupDisplayName() {
        return !this.isSet && this.impliedGroup
            ? this.groups[this.impliedGroup.id].name
            : "";
    }

    /**
     * CSS class map for the info/warning button next to the privilege field.
     * @returns {Record<string, boolean>}
     */
    get infoButtonClassnames() {
        const invisible = !this.isSet && !this.impliedGroup?.id;
        const isDisjoint = this.isDisjoint;
        return {
            o_group_info_button: true,
            invisible,
            btn: true,
            "btn-link": true,
            "py-0": true,
            fa: true,
            "fa-info-circle": !isDisjoint,
            "fa-exclamation-triangle": isDisjoint,
            "link-danger": isDisjoint,
        };
    }

    /**
     * Whether this privilege's active group conflicts with another active group.
     * @returns {boolean}
     */
    get isDisjoint() {
        return this.disjointGroupIds.length > 0;
    }

    /**
     * Whether this privilege is implied by another group (not explicitly selected).
     * @returns {boolean}
     */
    get isImplied() {
        return !this.isSet && !!this.impliedGroup;
    }

    /**
     * Whether this privilege field has a truthy value (group explicitly selected).
     * @returns {boolean}
     */
    get isSet() {
        return !!this.props.record.data[this.props.name];
    }

    /**
     * Field type of this privilege ("selection" or "boolean").
     * @returns {"selection" | "boolean"}
     */
    get type() {
        return this.props.record.fields[this.props.name].type;
    }

    /**
     * Find a group ID matching the predicate among this privilege's options.
     * For selection fields, searches from highest to lowest level.
     * For boolean fields, tests the single mapped group ID.
     * @param {(gid: number) => boolean} predicate - test function receiving a group ID
     * @returns {number | false} matching group ID, or false
     */
    findGroupId(predicate) {
        if (this.type === "selection") {
            const options = this.props.record.fields[this.props.name].selection;
            const option = options.findLast((o) => o[0] && predicate(o[0]));
            return option ? option[0] : false;
        } else {
            const groupId =
                this.env.resUserGroupsInfo.booleanFieldToGroupId[this.props.name];
            return predicate(groupId) ? groupId : false;
        }
    }

    /**
     * Toggle the info/warning popover showing group implications and conflicts.
     * @param {MouseEvent} ev
     */
    onClickInfoButton(ev) {
        if (this.popover.isOpen) {
            this.popover.close();
        } else {
            this.popover.open(ev.currentTarget, {
                groupId: this.group ? this.group.id : this.impliedGroup.id,
                groups: this.env.resUserGroupsInfo.groups,
                privileges: this.env.resUserGroupsInfo.privileges,
            });
        }
    }
}

const resUserGroupIdsPrivilegeField = {
    component: ResUserGroupIdsPrivilegeField,
};

registry
    .category("fields")
    .add("res_user_group_ids_privilege", resUserGroupIdsPrivilegeField);
