from odoo import api, fields, models
from odoo.exceptions import UserError


class L10n_Vn_SinvoiceSymbol(models.Model):
    _name = 'l10n_vn.sinvoice.symbol'
    _inherit = ['mail.thread']
    _description = 'SInvoice symbol'
    """
    The invoice symbols are made of multiple parts.
    The symbols characters have meanings that will influence the resulting invoices:

    The first character is either C (invoice with code from tax department) or K (without)
    It is then followed by two number which represent the current year (24 in 2024)
    Next, a single character representing additional invoice information:

        T: E-invoices registered with tax authorities by enterprises, organizations or household businesses;
        D: E-invoices used for sale of public property or national reserve goods or special e-invoices that do not have some mandatory contents of the "T" invoices;
        L: E-invoices issued separately by tax authorities;
        M: E-invoices generated by cash registers;
        N: Electronic delivery and internal consignment notes;
        B: Electronic delivery notes for goods sent to sales agents;
        G: VAT invoices in the form of electronic stamps, tickets or cards;
        H: Sales invoices in the form of electronic stamps, tickets or cards.

    Finally, it ends with two characters that shall be decided by the seller for their management requirements.
    """

    name = fields.Char(
        string='Symbol',
        required=True,
        tracking=True,
    )
    invoice_template_code = fields.Char(
        string='Template Code',
        required=True,
    )
    company_id = fields.Many2one(
        'res.company',
        string='Company',
        required=True,
        default=lambda self: self.env.company,
        tracking=True,
    )
    active = fields.Boolean(
        default=True,
        tracking=True,
    )
    usage = fields.Selection(
        selection=[
            ('invoice', 'Invoice'),
            ('vendor_bill', 'Vendor Bill'),
        ],
        string='Usage',
        required=True,
        default='invoice',
        tracking=True,
    )
    move_ids = fields.One2many(
        comodel_name='account.move',
        inverse_name='l10n_vn_sinvoice_symbol_id',
    )
    is_used = fields.Boolean(
        string='Used',
        compute='_compute_is_used',
        store=True,
    )
    is_fetched = fields.Boolean(
        string='Fetched',
        readonly=True,
        default=False,
    )

    _name_template_uniq = models.Constraint(
        'unique (name, usage, invoice_template_code, company_id)',
        'The combination symbol/template/usage must be unique per company!',
    )

    @api.depends('move_ids', 'move_ids.state')
    def _compute_is_used(self):
        used_symbol_ids = set()
        if self.ids:
            used_symbol_ids = {
                symbol.id
                for [symbol] in self.env['account.move']._read_group(
                    domain=[
                        ('l10n_vn_sinvoice_symbol_id', 'in', self.ids),
                        ('state', '=', 'posted'),
                        ('l10n_vn_sinvoice_symbol_id.is_used', '=', False),
                    ],
                    groupby=['l10n_vn_sinvoice_symbol_id'],
                )
                if symbol
            }
        for symbol in self:
            symbol.is_used = symbol._origin.id in used_symbol_ids

    @api.constrains('name', 'usage', 'invoice_template_code', 'company_id')
    def _constrains_is_symbol_used(self):
        if self.filtered(lambda symbol: symbol.is_fetched or symbol.is_used):
            raise UserError(self.env._(
                'You cannot modify the symbol, usage, template code or company of a fetched or already used e-invoice symbol.'
            ))

    @api.ondelete(at_uninstall=False)
    def _unlink_if_used(self):
        if self.filtered(lambda symbol: symbol.is_fetched or symbol.is_used):
            raise UserError(self.env._('You cannot delete a symbol that is fetched or used on a posted invoice or vendor bill. You can archive it instead.'))

    @api.depends('name', 'usage', 'invoice_template_code')
    def _compute_display_name(self):
        """ As we allow multiple of the same symbol name, we need to also display the template to differentiate. """
        for symbol in self:
            symbol.display_name = f'{symbol.name} ({symbol.invoice_template_code})'
