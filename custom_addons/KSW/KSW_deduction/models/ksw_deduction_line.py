from datetime import date

from dateutil.relativedelta import relativedelta
from markupsafe import Markup

from odoo import _, api, fields, models
from odoo.exceptions import UserError, ValidationError


# Fields that constitute a post-confirmation "installment modification"
# (year/month shift the period, amount changes the deducted value).
# Editing any of these is gated by the
# "Loan Installment Modification" privilege (group_installment_edit)
# and the line must be in state='pending'.
_INSTALLMENT_EDIT_KEYS = frozenset({'year', 'month', 'amount'})


class KswDeductionLine(models.Model):
    _name = 'ksw.deduction.line'
    _description = 'KSW Deduction Installment Line'
    _order = 'deduction_id, sequence, year, month'

    deduction_id = fields.Many2one(
        'ksw.deduction', required=True, ondelete='cascade',
        string='Deduction',
    )
    sequence = fields.Integer(string='#', default=1)
    year = fields.Integer(required=True)
    month = fields.Integer(required=True, help='Month number (1-12)')
    period_date = fields.Date(
        compute='_compute_period_date', store=True,
        help='First day of (year, month) — used for payslip period matching.',
    )
    amount = fields.Monetary(required=True)
    currency_id = fields.Many2one(
        related='deduction_id.currency_id', store=True, readonly=True,
    )
    state = fields.Selection([
        ('pending', 'Pending'),
        ('paid', 'Paid'),
        ('skipped', 'Skipped'),
    ], default='pending', required=True, copy=False)
    payslip_id = fields.Many2one(
        'hr.payslip', readonly=True, copy=False,
        help='Payslip that consumed this installment.',
    )
    # --------------------------------------------------------------
    # Manual-entry fields
    # --------------------------------------------------------------
    # Installments come from two sources:
    #   • AUTO — generated by `_generate_installment_lines` when the
    #     loan is activated. These get consumed by `hr.payslip` at
    #     run time (state 'pending' → 'paid' via KSW_DED_* input
    #     codes) and carry a `payslip_id` reference.
    #   • MANUAL — added by the accountant on an already-active loan
    #     to record payments collected OUTSIDE payroll (cash, bank
    #     transfer, etc.). Manual lines are created in state 'paid'
    #     directly — they never flow through a payslip and never get
    #     a `payslip_id`. The user and timestamp of the accountant
    #     who recorded them are captured in `manual_by` / `manual_date`
    #     so the audit trail is preserved and the payslip column in
    #     the UI can show "Manual by <user>" instead of a link.
    is_manual = fields.Boolean(
        string='Manual Entry', default=False, copy=False, readonly=True,
        help="Ticked for installment lines added by hand by the "
             "accountant (payment collected outside payroll). "
             "Auto-generated schedule lines stay False.",
    )
    manual_by = fields.Many2one(
        'res.users', string='Collected By', readonly=True, copy=False,
        help='Accountant who recorded this manual payment.',
    )
    manual_date = fields.Date(
        string='Collected On', readonly=True, copy=False,
        help='Date the manual payment was recorded.',
    )
    manual_note = fields.Char(
        string='Note', copy=False,
        help='Free-text note for the manual payment (reference '
             'number, collection channel, etc.).',
    )
    # Combined display used in the "Settlement" column of the
    # installments list: either the payslip name (for auto-paid
    # lines) or "Manual by <user> on <date>" (for manual lines).
    # Left blank for pending lines.
    settlement_label = fields.Char(
        string='Settlement',
        compute='_compute_settlement_label',
        help='"Payslip <ref>" for auto-paid lines, '
             '"Manual by <user> on <date>" for manual lines.',
    )
    # Display helpers
    employee_id = fields.Many2one(
        related='deduction_id.employee_id', store=True, readonly=True,
    )
    type_id = fields.Many2one(
        related='deduction_id.type_id', store=True, readonly=True,
    )

    _valid_month = models.Constraint(
        'CHECK(month >= 1 AND month <= 12)',
        'Month must be between 1 and 12.',
    )

    @api.depends('year', 'month')
    def _compute_period_date(self):
        for line in self:
            if line.year and line.month:
                line.period_date = fields.Date.to_date(
                    '%04d-%02d-01' % (line.year, line.month))
            else:
                line.period_date = False

    @api.depends('is_manual', 'manual_by', 'manual_date',
                 'payslip_id', 'payslip_id.number', 'state')
    def _compute_settlement_label(self):
        """Human-readable settlement descriptor for the installment.

        Visible in the Installments list under the "Settlement"
        column and used in reports / payslip views that reference
        the line. Three cases:
          • pending  → ''
          • paid via payslip → payslip.number (or display name)
          • paid manually     → 'Manual by <user> on <date>'
        """
        for line in self:
            if line.is_manual and line.state == 'paid':
                who = line.manual_by.name or _('Unknown user')
                when = line.manual_date and fields.Date.to_string(
                    line.manual_date) or ''
                line.settlement_label = _(
                    "Manual by %(u)s%(d)s",
                    u=who,
                    d=(' on %s' % when) if when else '',
                )
            elif line.payslip_id:
                line.settlement_label = (
                    line.payslip_id.number
                    or line.payslip_id.display_name
                    or ''
                )
            else:
                line.settlement_label = ''

    def name_get(self):
        res = []
        month_names = ['', 'Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun',
                       'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec']
        for line in self:
            label = '%s %d' % (
                month_names[line.month] if 1 <= line.month <= 12 else '?',
                line.year or 0,
            )
            res.append((line.id, label))
        return res

    # ------------------------------------------------------------------
    # Post-confirmation installment modification
    # ------------------------------------------------------------------
    # Gated by the "Loan Installment Modification" privilege
    # (group_installment_edit). Applies whenever year/month/amount is
    # changed on an individual installment. Rules:
    #   1. The line must be in state='pending' (paid/skipped are frozen).
    #   2. The user must hold group_installment_edit — unless the
    #      write is happening in sudo / from internal generation code
    #      (create() flows do not go through this branch).
    #   3. If the line's period moves FORWARD (new period_date >
    #      old period_date), every subsequent pending line on the
    #      same deduction is shifted by the same month-delta so the
    #      schedule stays contiguous. Backward or same-month edits
    #      do NOT cascade (the accountant can only postpone, not
    #      pull forward).
    # ------------------------------------------------------------------
    def write(self, vals):
        if not (_INSTALLMENT_EDIT_KEYS & set(vals)):
            return super().write(vals)

        # --- 1. State guard ---------------------------------------------
        # Only PENDING lines (auto-schedule) and MANUAL PAID lines can
        # be modified. Auto-paid lines (linked to a payslip) and
        # skipped lines are frozen.
        for line in self:
            modifiable = (
                line.state == 'pending'
                or (line.is_manual and line.state == 'paid')
            )
            if not modifiable:
                raise UserError(_(
                    "Installment %(label)s on loan %(loan)s is "
                    "'%(state)s' and cannot be modified. Only pending "
                    "installments (or manual paid entries) can be "
                    "rescheduled or re-valued.",
                    label=line.display_name,
                    loan=line.deduction_id.name or '',
                    state=dict(line._fields['state'].selection).get(
                        line.state, line.state),
                ))

        # --- 2. Privilege guard -----------------------------------------
        if not self.env.su:
            user = self.env.user
            if not user.has_group('KSW_deduction.group_installment_edit'):
                raise UserError(_(
                    "You do not have the 'Loan Installment Modification' "
                    "privilege. Ask an administrator to grant it on your "
                    "user (Settings → Users → Access Rights → KSW "
                    "Deductions → Loan Installment Modification = Edit)."
                ))

        # --- 3. Snapshot per-line old (year, month) so we can compute
        #        the month-delta AFTER super().write() ------------------
        # Map: line.id -> (old_year, old_month)
        old_ym = {line.id: (line.year, line.month) for line in self}

        res = super().write(vals)

        # --- 4. Cascade: for each edited line whose period moved
        #        FORWARD, shift subsequent pending lines on the same
        #        deduction by the same month-delta. Use a dict keyed by
        #        deduction to avoid double-shifting when the caller
        #        edits several lines at once on the same loan — in
        #        that case we apply the max positive delta starting
        #        from the earliest changed sequence.
        #        Manual paid edits do NOT cascade (they're bookkeeping
        #        entries, not schedule pivots). ------------------------
        shifts = {}  # deduction_id -> (min_sequence_touched, delta_months)
        for line in self:
            if line.is_manual:
                continue
            old_y, old_m = old_ym[line.id]
            new_y, new_m = line.year, line.month
            delta = (new_y - old_y) * 12 + (new_m - old_m)
            if delta <= 0:
                continue  # same month or pulled backward: no cascade
            prev = shifts.get(line.deduction_id.id)
            if prev is None or line.sequence < prev[0]:
                shifts[line.deduction_id.id] = (line.sequence, delta)
            elif line.sequence == prev[0] and delta > prev[1]:
                shifts[line.deduction_id.id] = (line.sequence, delta)

        for ded_id, (pivot_seq, delta) in shifts.items():
            siblings = self.search([
                ('deduction_id', '=', ded_id),
                ('state', '=', 'pending'),
                ('is_manual', '=', False),
                ('sequence', '>', pivot_seq),
                ('id', 'not in', self.ids),
            ])
            for sib in siblings:
                try:
                    base = date(sib.year, sib.month, 1)
                except ValueError:
                    continue
                new_date = base + relativedelta(months=delta)
                # super().write on a single record bypasses this
                # override → no recursion, no guards re-triggered.
                super(KswDeductionLine, sib).write({
                    'year': new_date.year,
                    'month': new_date.month,
                })
            # Chatter trail on the loan so the change is auditable.
            ded = self.env['ksw.deduction'].browse(ded_id)
            ded.message_post(
                body=Markup(
                    '<strong>📅 Installments Rescheduled</strong><br/>'
                    '<b>Pivot installment #:</b> %(seq)d<br/>'
                    '<b>Shift:</b> +%(d)d month(s)<br/>'
                    '<b>Cascaded lines:</b> %(n)d<br/>'
                    '<b>By:</b> %(u)s'
                ) % {
                    'seq': pivot_seq,
                    'd': delta,
                    'n': len(siblings),
                    'u': self.env.user.name,
                },
                subtype_xmlid='mail.mt_note',
            )

        return res

    # ------------------------------------------------------------------
    # Total-consistency guard
    # ------------------------------------------------------------------
    # The sum of non-skipped installment amounts (pending + paid) must
    # always equal the parent deduction's total `amount`. This catches
    # any post-confirmation edit that would under- or over-charge the
    # employee — e.g. lowering a single installment from 200 → 100
    # without raising another one to compensate.
    #
    # Enforced only while the deduction is 'active' or 'completed'.
    # During 'draft' the parent has no lines yet; 'cancelled' lines
    # are marked 'skipped' and explicitly excluded from the sum.
    #
    # ⚠️  Timing: Odoo's base `write()` ends with `_validate_fields`,
    # so @api.constrains fires **after every individual line write**.
    # When the form saves multiple O2M edits (e.g. line #5: 200→150
    # AND line #6: 200→250), Odoo dispatches them as separate writes,
    # so the constraint would see a *partial* state (total short by
    # 50) after the first write and reject the save even though the
    # net change is balanced. To avoid this, `ksw.deduction.write()`
    # sets `_skip_installment_total_check` in the context while it
    # dispatches the batched O2M commands and then calls
    # `ksw.deduction._validate_installments_total()` ONCE at the end
    # on the full, post-write state. Direct line writes (outside a
    # parent save) still go through the constraint normally.
    # ------------------------------------------------------------------
    @api.constrains('amount', 'state', 'deduction_id')
    def _check_total_matches_deduction_amount(self):
        if self.env.context.get('_skip_installment_total_check'):
            return
        # Delegate to the parent so the exact same validation runs
        # whether called from a line-level constrain or from the
        # deferred post-batch hook in KswDeduction.write().
        self.mapped('deduction_id')._validate_installments_total()

    # ==================================================================
    # Manual-entry support
    # ==================================================================
    # There are exactly two code paths that create `ksw.deduction.line`
    # records:
    #
    #   1. AUTO — the parent's `_generate_installment_lines()` runs
    #      on activation. It sets `_ksw_auto_generating=True` in the
    #      env context before dispatching the x2m `(0, 0, {...})`
    #      commands, so we recognise it here and leave those creates
    #      untouched (they default to `is_manual=False`, `state='pending'`).
    #
    #   2. MANUAL — any other `create()` call on an active loan
    #      (typically the accountant adding a row to the editable
    #      installments list). We require `group_installment_edit`
    #      and force `is_manual=True`, `state='paid'`, stamping
    #      `manual_by`/`manual_date` so the audit trail is preserved.
    #      A `payslip_id` is never allowed on a manual line.
    #
    # Superuser / sudo bypasses the manual-path checks so tests,
    # migrations and internal fixtures keep working.
    # ==================================================================
    @api.model_create_multi
    def create(self, vals_list):
        if (self.env.context.get('_ksw_auto_generating')
                or self.env.su):
            return super().create(vals_list)

        user = self.env.user
        if not user.has_group('KSW_deduction.group_installment_edit'):
            raise UserError(_(
                "Adding a manual installment requires the 'Loan "
                "Installment Modification' privilege. Ask an "
                "administrator to grant it on your user "
                "(Settings → Users → Access Rights → KSW Deductions "
                "→ Loan Installment Modification = Edit)."
            ))
        today = fields.Date.context_today(self)
        # Pre-compute max-sequence per parent so the batch stays linear.
        seq_per_ded = {}
        for vals in vals_list:
            if vals.get('payslip_id'):
                raise UserError(_(
                    "Manual installment entries cannot be linked to a "
                    "payslip — they represent payments collected "
                    "outside the payroll system."
                ))
            vals['is_manual'] = True
            vals['state'] = 'paid'
            vals.setdefault('manual_by', user.id)
            vals.setdefault('manual_date', today)
            if 'sequence' not in vals:
                ded_id = vals.get('deduction_id')
                if ded_id:
                    if ded_id not in seq_per_ded:
                        ded = self.env['ksw.deduction'].browse(ded_id).sudo()
                        seq_per_ded[ded_id] = max(
                            ded.line_ids.mapped('sequence') or [0],
                            default=0,
                        )
                    seq_per_ded[ded_id] += 1
                    vals['sequence'] = seq_per_ded[ded_id]

        lines = super().create(vals_list)

        # Chatter trail on each parent deduction (one post per loan,
        # even if the batch created several manual lines for it).
        for ded in lines.mapped('deduction_id'):
            mine = lines.filtered(lambda l: l.deduction_id == ded)
            body = [
                '<strong>💵 Manual Payment(s) Recorded</strong><br/>',
                '<b>By:</b> %s<br/>' % (user.name or ''),
                '<b>Lines:</b><br/>',
            ]
            for ml in mine:
                body.append(
                    '&nbsp;&nbsp;• %s — %.2f %s%s<br/>' % (
                        ml.display_name,
                        ml.amount,
                        ded.currency_id.name or '',
                        (' (%s)' % ml.manual_note) if ml.manual_note else '',
                    )
                )
            ded.message_post(
                body=Markup(''.join(body)),
                subtype_xmlid='mail.mt_note',
            )
        return lines

    def unlink(self):
        """Allow deletion of MANUAL lines by privileged users only;
        auto-generated lines can only be removed via the parent's
        cancel / reset-to-draft flow. Paid auto lines (linked to a
        payslip) are hard-frozen — reset the payslip first."""
        su = self.env.su
        user = self.env.user
        for line in self:
            if line.state == 'paid' and line.payslip_id and not su:
                raise UserError(_(
                    "Installment %(label)s has been paid via payslip "
                    "%(slip)s and cannot be deleted. Reset the "
                    "payslip to draft first if you need to undo it.",
                    label=line.display_name,
                    slip=line.payslip_id.display_name or '',
                ))
            if not line.is_manual and not su:
                raise UserError(_(
                    "Installment %(label)s is part of the auto-"
                    "generated schedule. It cannot be deleted directly "
                    "— cancel the whole loan or reset it to draft to "
                    "regenerate the schedule.",
                    label=line.display_name,
                ))
            if (line.is_manual
                    and not su
                    and not user.has_group(
                        'KSW_deduction.group_installment_edit')):
                raise UserError(_(
                    "Deleting a manual installment requires the 'Loan "
                    "Installment Modification' privilege."
                ))
        return super().unlink()

